          Responsabilidade
        </h2>

        <p>
          O site COMPLEXO GALLERIA pertence a Cariba Empreendimentos e Participações Ltda (47.877.659/0001-94).
          Reconhecemos a importância dos seus dados e de sua privacidade, e levamos isso a sério. Por isso, assumimos o compromisso com você, que navega, interage conosco e nos fornece os seus dados.
          A Cariba é a controladora dos dados pessoais que processa e responsável por garantir que os sistemas e processos estejam em conformidade com as leis de proteção de dados, em especial com a Lei nº 13.709/2018 (“Lei Geral de Proteção de Dados Pessoais” ou “LGPD”) na medida em que forem a nós aplicáveis.
        </p>

        <h2>
          Coleta dos dados
        </h2>

        <p>
          Coletamos dados pessoais de várias fontes com relação à administração diária de nossos negócios e relações comerciais.

          Coletamos dados pessoais de várias fontes, diretamente dos titulares ou de clientes, parceiros e fontes publicamente disponíveis.

          Quando recebermos dados de nossos clientes ou parceiros ou prestadores de serviços, sobre seus funcionários, seus clientes ou outros indivíduos, os nossos clientes, parceiros ou prestadores de serviços são responsáveis por garantir que esses dados nos sejam transferidos em conformidade com as leis de proteção de dados aplicáveis.

          Podemos vir a coletar:

        <ul>
          <li>
            Dados básicos: nome, telefone, endereço para correspondência, endereço de e-mail, entre outros.

          </li>
          <li>
            Dados contidos em documentos de identidade: comprovante de inscrição pessoa física, registro geral, data de nascimento, entre outros.

          </li>
          <li>
            Dados de marketing: dados sobre presença individual em estandes de vendas, participação em conferências e seminários, credenciais, associações, interesses em produtos e preferências.
          </li>

          <li>
            Dados de candidatos a emprego: informações fornecidas por candidatos a uma oferta de emprego.
          </li>

          <li>
            Dados de dispositivos eletrônicos: endereço de IP, cookies, entre outros.
          </li>
        </ul>
        </p>

        <h3>
          Uso dos dados
        </h3>

        <p>
          Usamos os dados pessoais coletados para prestar nossos serviços, para gerenciar nossas operações comerciais, administrativas e financeiras, para desenvolver trabalhos de marketing, e para cumprir outros objetivos profissionais, sempre que em conformidade com as leis aplicáveis.

          As finalidades para as quais usamos os dados pessoais que coletamos e as bases legais para esse processamento são:

        <ul>
          <li>
            Gerenciar nossas operações comerciais e administrar nossos relacionamentos com os clientes: usamos dados básicos, dados contidos em documentos de identidade e dados de marketing. Esse processamento é necessário para cumprir nossas obrigações de acordo com a natureza dos nossos projetos e contratos com clientes e prestadores de serviço.
          </li>

          <li>
            Tornar nossos sites mais intuitivos e fáceis de usar: usamos dados de dispositivos eletrônicos para nos ajudar a melhorar os nossos sites e fornecer um melhor serviço aos usuários de nossos sites.
          </li>

          <li>
            Fornecer marketing sobre nossos produtos: usamos dados de marketing, dados básicos, dados contidos em documentos de identidade, dados de dispositivos eletrônicos, para fornecer aos nossos clientes, atuais e potenciais, informações sobre eventos ou produtos que possam ser de seu interesse.
          </li>

          <li>
            Atender às obrigações legais: podemos usar os dados básicos, dados contidos em documentos de identidade, dados de dispositivos eletrônicos, para verificar a identidade de novos ou atuais clientes, parceiros e fornecedores, e evitar lavagem de dinheiro e/ou algum tipo de fraude, e para o cumprimento das exigências legais às quais podemos estar sujeitos.
          </li>

          <li>
            Considerar candidatos a oportunidades de emprego e contratação: usamos dados de candidatos a emprego para fins de recrutamento, seleção e contratação, para cumprir as obrigações legais às quais estamos sujeitos.
          </li>
        </ul>
        </p>

