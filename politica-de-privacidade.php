          Compartilhamento dos dados
        </h3>

        <p>
          Podemos compartilhar dados pessoais com fornecedores de prestadores de serviços.

          Compartilhamos dados pessoais com fornecedores e prestadores de serviços para permitir que essas partes desempenhem funções em nosso nome segundo nossas instruções para atingir as finalidades acima neste aviso identificadas. Eles podem ser: provedores de infraestrutura e serviços de TI, serviços de captação de clientes, instituições financeiras, serviços terceirizados de análise de negócio e campanhas de marketing. Exigimos que essas partes, por meio de contrato, forneçam segurança para os dados pessoais, usem e processem esses dados pessoais apenas em nosso nome,

          Caso você tenha dúvidas sobre as partes com as quais compartilhamos os dados pessoais, entre em contato conosco pelo e-mail <a href="mailto:encarregado@cariba.com.br">encarregado@cariba.com.br</a>.

        </p>

        <h3>
          Segurança dos dados
        </h3>

        <p>
          Implementamos medidas para salvaguardar os dados pessoais que estão sob nossa responsabilidade.

          Mesmo assim não podemos garantir que informações armazenadas em nosso sistema ou quando transmitidas estejam absolutamente protegidas de terceiros mal intencionados.

          Você também tem um papel importante na proteção dos dados pessoais: não deve compartilhar com ninguém informações como usuário e senha, ou outras referências de autenticação. Recomendamos que você não reutilize senhas em diferentes websites ou aplicativos e, se você tiver algum motivo para acreditar que seu nome, informação de usuário, senha foram expostos, entre em contato conosco no e-mail <a href="mailto:encarregado@cariba.com.br">encarregado@cariba.com.br</a>.
        </p>

        <h3>
          Direitos do titular dos dados
        </h3>

        <p>
          Você como titular dos seus dados pessoais tem os seguintes direitos:
        <ul>
          <li>
            Confirmar conosco a existência de tratamento dos seus dados pessoais pela Cariba.
          </li>
          <li>
            Solicitar, quando aplicável, uma cópia dos seus dados pessoais que estamos tratando, que forneceremos em formato eletrônico.
          </li>

          <li>
            Pedir para que os seus dados pessoais que estamos tratando, quando desatualizados, incompletos ou imprecisos, sejam corrigidos.
          </li>

          <li>
            Solicitar a anonimização, o bloqueio ou a eliminação dos seus dados quando desnecessários, excessivos ou tratados em desconformidade com a LGPD.
          </li>

          <li>
            Pedir a exclusão de seus dados pessoais tratados com o seu consentimento, caso não sejamos obrigados a retermos esses dados para cumprir uma obrigação legal ou para apresentar defesa em ações judiciais.
          </li>

          <li>
            Solicitar para transferirmos os seus dados pessoais que estamos tratando para outro controlador se: forem suas informações pessoais que você nos tiver fornecido e estivermos processando esses dados com base em seu consentimento, ou para cumprir nossas obrigações contratuais com você.
          </li>

          <li>
            Discordar do processamento com base na sua situação específica, sempre que a justificativa legal para o nosso processamento de seus dados pessoais for nosso interesse legítimo. A menos que tenhamos motivos legítimos e convincentes para o processamento que se sobreponham a seus interesses e direitos, ou se precisarmos seguir processando os dados por uma questão do próprio negócio ou legal.
          </li>

          <li>
            Solicitar informações das entidades públicas e privadas com as quais nós compartilhamos os seus dados pessoais.
          </li>

          <li>
            Solicitar informação sobre a possibilidade de não fornecer o seu consentimento para o tratamento de seus dados pessoais e sobre as consequências de não consentir.
          </li>

          <li>
            Retirar o seu consentimento, uma vez que você consentiu o nosso processamento dos seus dados pessoais, a qualquer momento e gratuitamente, incluindo o fato de você ter optado por não receber mensagens de marketing que enviamos.
          </li>
        </ul>
        </p>

